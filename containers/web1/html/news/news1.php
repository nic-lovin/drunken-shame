<h3>PQ: Cloutier et Ouellet veulent une course à la diète</h3>

<p>(Québec) Les députés Martine Ouellet et Alexandre Cloutier ont confirmé, ce matin, avoir demandé aux dirigeants du Parti
québécois (PQ) de diviser par deux la limite de dépenses par candidat pour l'imminente course à la direction du parti.

<p>Les deux probables candidats se défendent bien de vouloir nuire au multimillionnaire Pierre Karl Péladeau en militant
pour le passage d'un budget maximal de 500 000 $ à un budget maximal de 250 000 $.

<p>«On ne veut pas une course à l'argent, on veut une course aux idées», a expliqué Alexandre Cloutier ce matin, confirmant
une information du Journal de Québec. «Est-ce que vraiment on a besoin d'un demi-million par candidat pour faire une
course à la direction?»

<p>«C'est important que cette course soit réalisée sur l'égalité des chances», ajoute la cosignataire de la lettre, Martine
Ouellet. 

<p>La lettre demande aussi de fixer la contribution maximale de chaque électeur à 200 $ plutôt que 500 $, a dit Mme
Ouellet.

<p>Aucun lien avec la présence probable d'un homme d'affaires fortuné dans la course. «Ça n'a absolument rien à voir», a
fait valoir M. Cloutier, rappelant qu'il s'était réjoui de l'arrivée de M. Péladeau au sein de sa formation politique.

<p>Plusieurs des autres potentiels candidats ont refusé de se prononcer sur la question. 

<p>Bernard Drainville a demandé plus de temps pour réfléchir, alors que Pierre Karl Péladeau a - comme à son habitude -
refusé de répondre aux questions des journalistes. 

<p>«C'est un beau débat», s'est limité à dire Jean-François Lisée. Même son de cloche du côté de Véronique Hivon.
