<h3>L'EI exécute «plus de 160 soldats syriens»</h3>

Les djihadistes de l'État islamique (EI) ont exécuté mercredi et jeudi plus de 160 soldats syriens qu'ils détenaient
dans le nord du pays, a affirmé à l'AFP le directeur de l'Observatoire syrien des droits de l'homme (OSDH) Rami Abdel
Rahmane.

«L'EI a exécuté entre hier et aujourd'hui à l'aube plus de 160 soldats syriens dans trois lieux différents de la
province de Raqa, dans le nord de la Syrie», a-t-il dit.

Sur Twitter, des comptes attribués à djihadistes (ou à des sympathisants) font état de 200 morts. 

«Des dizaines de soldats syriens, capturés alors qu'ils fuyaient vers Esraya, dans la province de Hama, après la prise
par l'EI de leur base aérienne de Tabqa, ont été exécutés par les djihadistes dans la nuit de mercredi à jeudi», a
indiqué à l'AFP Rami Abdel Rahmane, directeur de l'Observatoire syrien des droits de l'homme (OSDH).

Selon lui, 1400 soldats défendaient la base de Tabqa, conquise dimanche par les ultra-radicaux de l'EI. Durant les
combats, près de 200 soldats sont morts, et environ 700 ont réussi à rejoindre des secteurs contrôlés par le régime.

Sur les 500 restant, une partie continue à se battre dans la ferme Ajraoui, près de la base de Tabqa, une autre se cache
et plusieurs dizaines ont été capturés dans la nuit de mercredi à jeudi alors qu'ils essayaient de fuir en empruntant
les 50 km de route désertique les séparant de la localité d'Esraya, dans la province de Hama, aux mains du régime,
a-t-il précisé.

